#include <iostream>

using namespace std;

int main() {
    // inisialisasi X dengan nilai 5
    int X = 5;

    // menampilkan nilai X sebelum dilakukan pengisian nilai (assignment)
    cout << "Nilai X sebelum assignment: " << X << endl;

    // mengisi nilai 10 ke dalam variabel X
    X = 10;

    // menampilkan nilai X setelah dilakukan pengisian nilai (assignment)
    cout << "Nilai X setelah assignment: " << X << endl;

    return 0;
}
