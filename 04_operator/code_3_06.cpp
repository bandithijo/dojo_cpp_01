#include <iostream>

using namespace std;

int main() {
    // mendeklarasikan variabel X (diisi nilai 10) dan Y (diisi nilai 3)
    int X = 10, Y = 3;
    // mendeklarasikan variabel Z sebagai penampung nilai hasil operasi
    int Z;

    // melakukan operasi penjumlahan
    Z = X + Y;

    // menampilkan hasil penjumlahan
    cout << X << " + " << Y << " = " << Z << endl;

    return 0;
}
