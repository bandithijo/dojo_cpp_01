#include <iostream>

using namespace std;

int main() {
    // mendeklarasikan variabel X (diisi nilai 10) dan Y (diisi nilai 3)
    int X = 10, Y = 3;
    // mendeklarasikan vairabel Z sebagai penampung nilai hasil operasi
    int Z;

    // melakukan operasi pengurangan
    Z = X - Y;

    // menampilkan hasil pengurangan
    cout << X << " - " << Y << " = " << Z << endl;

    return 0;
}
